let a = prompt('a ədədini daxil edin')
let emr = prompt('Isədiyiniz riyazi əməliyyatı daxil edin')
let b = prompt('b ədədini daxil edin')

if (emr == '+'){
    let c = Number(a) + Number(b)
    alert(c)
}

if (emr == '-'){
    let c = Number(a) - Number(b)
    alert(c)
}

if (emr == '*'){
    let c = Number(a) * Number(b)
    alert(c)
}

if (emr == '/'){
    let c = Number(a) / Number(b)
    alert(c)
}
